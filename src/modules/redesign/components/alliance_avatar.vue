<template>
    <div>
        <h1>{{ lightbox.$sm('title') }}</h1>
        <div class="well pull-right" v-if="alliance.image">
            <b>{{ lightbox.$sm('current') }}</b>
            <button class="btn btn-danger pull-right" @click="deleteAvatar">
                <font-awesome-icon :icon="faTrash"></font-awesome-icon>
            </button>
            <br />
            <img :src="alliance.image" alt="" loading="lazy" />
        </div>
        <button @click="select" class="btn btn-success">
            {{ lightbox.$sm('select') }}
        </button>
        <button @click="submit" class="btn btn-success" :disabled="!imageFile">
            {{ lightbox.$sm('save') }}
        </button>
        <br />
        <b v-if="image">{{ lightbox.$sm('preview') }}:</b>
        <br />
        <img :src="image" alt="" />
    </div>
</template>

<script lang="ts">
import Vue from 'vue';

import { faTrash } from '@fortawesome/free-solid-svg-icons/faTrash';
import { useEventStore } from '@stores/event';

import type { IconDefinition } from '@fortawesome/fontawesome-svg-core';
import type { RedesignComponent } from 'typings/modules/Redesign';

type Component = RedesignComponent<
    'alliance',
    'alliance_avatar',
    {
        faTrash: IconDefinition;
        image: string;
        imageFile: File | null;
        input: HTMLInputElement;
        eventStore: ReturnType<typeof useEventStore>;
    },
    {
        submit(): void;
        deleteAvatar(): void;
        select(): void;
    }
>;

export default Vue.extend<
    Component['Data'],
    Component['Methods'],
    Component['Computed'],
    Component['Props']
>({
    name: 'lssmv4-redesign-alliance-avatar-edit',
    data() {
        const input = document.createElement('input');
        input.type = 'file';
        input.accept = 'image/*';
        return {
            faTrash,
            image: '',
            imageFile: null,
            input,
            eventStore: useEventStore(),
        };
    },
    methods: {
        submit() {
            if (!this.imageFile) return;
            const formData = new FormData();
            formData.append('utf8', 'âœ“');
            formData.append('_method', 'put');
            formData.append(
                'authenticity_token',
                this.alliance.authenticity_token
            );
            formData.append(
                'alliance_avatar[image]',
                this.imageFile,
                this.imageFile.name
            );
            formData.append('commit', 'save');
            this.lightbox.apiStore
                .request(
                    `/verband/avatar/upload`,
                    `redesign-alliance-avatar-edit`,
                    {
                        credentials: 'include',
                        headers: {
                            'Accept':
                                'text/html,application/xhtml+xml,application/xml;q=0.9,image/webp,*/*;q=0.8',
                            'Upgrade-Insecure-Requests': '1',
                        },
                        referrer: new URL(
                            `verband/avatar`,
                            window.location.origin
                        ).toString(),
                        body: formData,
                        method: 'POST',
                        mode: 'cors',
                    }
                )
                .then(({ url }) => {
                    const img = this.image;
                    this.image = '';
                    this.imageFile = null;
                    if (
                        !new URL(
                            this.url,
                            window.location.origin
                        ).searchParams.has('close-after-submit') ||
                        this.lightbox.noModal
                    )
                        return this.$set(this.lightbox, 'src', url);
                    this.eventStore.createAndDispatchEvent({
                        name: 'redesign-edit-alliance-avatar-submitted',
                        detail: {
                            img,
                        },
                    });
                    window.lightboxClose(this.lightbox.creation);
                });
        },
        deleteAvatar() {
            this.$set(this.lightbox, 'loading', true);
            this.lightbox.apiStore
                .request(
                    `/verband/avatar/delete`,
                    `redesign-alliance-avatar-delete`,
                    {
                        credentials: 'include',
                        referrer: new URL(
                            `verband/avatar`,
                            window.location.origin
                        ).toString(),
                        method: 'GET',
                        mode: 'cors',
                    }
                )
                .then(() => {
                    this.$set(this.lightbox.data, 'image', '');
                    this.lightbox.finishLoading('avatar-deleted');
                    this.eventStore.createAndDispatchEvent({
                        name: 'redesign-edit-alliance-avatar-submitted',
                        detail: {
                            img: '',
                        },
                    });
                });
        },
        select() {
            this.input.click();
        },
    },
    props: {
        alliance: {
            type: Object,
            required: true,
        },
        url: {
            type: String,
            required: true,
        },
        lightbox: {
            type: Object,
            required: true,
        },
        getSetting: {
            type: Function,
            required: true,
        },
        setSetting: {
            type: Function,
            required: true,
        },
    },
    watch: {
        alliance() {
            this.lightbox.finishLoading('alliance-avatar-edit-updated');
        },
    },
    mounted() {
        this.input.addEventListener('change', () => {
            if (!this.input.files?.length) return;
            const reader = new FileReader();
            reader.addEventListener('load', () => {
                this.image = reader.result?.toString() ?? '';
                this.imageFile = this.input.files?.[0] ?? null;
            });
            // eslint-disable-next-line unicorn/prefer-add-event-listener
            reader.onerror = () => {
                this.image = '';
                this.imageFile = null;
            };
            reader.readAsDataURL(this.input.files[0]);
        });
        this.lightbox.finishLoading('alliance-avatar-edit-mounted');
    },
});
</script>

